package com.axono.domain;

/**
 * As 6 rotações fixas do internato médico.
 * Cada grupo percorre todas no ciclo (ex.: 6 bimestres).
 */
public enum RotationType {
    /** Cirurgia Geral */
    SURGERY,
    /** Pediatria */
    PEDIATRICS,
    /** Clínica Médica */
    CLINICAL,
    /** Ginecologia e Obstetrícia */
    GYNECOLOGY,
    /** Urgência e Emergência */
    EMERGENCY,
    /** Saúde Coletiva / APS */
    HEALTH
}
